export default async function BiblePage() {
  const session = await getSession();
  if (!session) redirect("/login");

  const categories = await prisma.category.findMany({
    where: { isActive: true },
    orderBy: { order: "asc" },
    include: {
      _count: { select: { articles: { where: { status: "PUBLISHED" } } } },
    },
  });

  const recentArticles = await prisma.article.findMany({
    where: { status: "PUBLISHED" },
    orderBy: { publishedAt: "desc" },
    take: 6,
    include: { category: { select: { name: true, slug: true } } },
  });

  return (
    <div className="space-y-6">
      <div>
        <h1 className="text-2xl font-bold text-slate-900">Company Bible</h1>
        <p className="text-slate-500 mt-1">Everything you need to know about how we operate</p>
      </div>

      {/* Categories grid */}
      <section>
        <h2 className="text-sm font-semibold text-slate-500 uppercase tracking-wider mb-3">Browse by Department</h2>
        <div className="grid grid-cols-2 sm:grid-cols-3 md:grid-cols-4 gap-3">
          {categories.map((cat) => (
            <Link
              key={cat.id}
              href={`/bible/category/${cat.slug}`}
              className="flex flex-col items-center gap-2 p-4 bg-white rounded-xl border border-slate-200 hover:border-brand-200 hover:shadow-sm transition-all text-center"
            >
              <div className="w-10 h-10 rounded-lg bg-brand-50 flex items-center justify-center">
                <svg className="w-5 h-5 text-brand-600" fill="none" viewBox="0 0 24 24" stroke="currentColor" strokeWidth={1.5}>
                  <path strokeLinecap="round" strokeLinejoin="round" d={DEPT_ICONS[cat.dept] ?? DEPT_ICONS.GENERAL} />
                </svg>
              </div>
              <div>
                <div className="text-sm font-semibold text-slate-900">{cat.name}</div>
                <div className="text-xs text-slate-400 mt-0.5">{cat._count.articles} articles</div>
              </div>
            </Link>
          ))}

          {categories.length === 0 && (
            <div className="col-span-full text-center py-8 text-slate-400 text-sm">
              No categories yet. Contact your administrator.
            </div>
          )}
        </div>
      </section>

      {/* Recent articles */}
      {recentArticles.length > 0 && (
        <section>
          <h2 className="text-sm font-semibold text-slate-500 uppercase tracking-wider mb-3">Recently Updated</h2>
          <div className="grid sm:grid-cols-2 gap-3">
            {recentArticles.map((article) => (
              <Link
                key={article.id}
                href={`/bible/${article.slug}`}
                className="p-4 bg-white rounded-xl border border-slate-200 hover:border-brand-200 hover:shadow-sm transition-all"
              >
                <div className="flex items-center gap-2 mb-2">
                  <span className="text-xs font-medium text-brand-600 bg-brand-50 px-2 py-0.5 rounded-full">
                    {article.category.name}
                  </span>
                  {article.isSample && (
                    <span className="text-xs text-slate-400 bg-slate-100 px-2 py-0.5 rounded-full">Sample</span>
                  )}
                </div>
                <p className="text-sm font-semibold text-slate-900 line-clamp-2">{article.title}</p>
              </Link>
            ))}
          </div>
        </section>
      )}
    </div>
  );
}
